DATA SEGMENT
	MONTH DB 'JAN','FEB','MAR','APR','MAY','JUN','JUL','AUG','SEP','OCT','NOV','DEC'
	NUM DB 2 DUP(?)
	STRONE DB 'month:?',10,'$'
	STRTWO DB 'month ERROR!',10,'$'
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA

START:
	MOV AX,DATA
	MOV DS,AX
BEGIN:
	MOV DX,OFFSET STRONE
	MOV AH,9
	INT 21H
	MOV BX,0
	MOV [NUM],BL
	MOV [NUM+1],BL
RE:
	MOV AH,1
	INT 21H
	CMP AL,1BH
	JE EXIT
	CMP AL,0DH
	JE NEXT
	CMP AL,30H
	JB ERROR
	CMP AL,39H
	JA ERROR
	CMP BX,2
	JZ ERROR
	MOV NUM[BX],AL
	INC BX
	JMP RE
NEXT:
	MOV AL,[NUM]
	SUB AL,30H
	JE ERROR
	MOV BL,[NUM+1]
	CMP BL,0
	JE GO
	SUB BL,30H
	MOV AH,10
	MUL AH
	ADD AL,BL
	CMP AL,12
	JA ERROR
GO:
	MOV CX,3
	DEC AL
	MOV AH,3
	MUL AH
	MOV BX,OFFSET MONTH
	ADD BX,AX
	MOV DL,10
	MOV AH,2
	INT 21H
OUTPUT:
	MOV DL,[BX]
	MOV AH,2
	INT 21H
	INC BX
	LOOP OUTPUT
	MOV DL,0AH
	MOV AH,2
	INT 21H
	JMP BEGIN
ERROR:
	MOV DL,0AH
	MOV AH,2
	INT 21H
	MOV DX,OFFSET STRTWO
	MOV AH,9
	INT 21H
	JMP BEGIN
EXIT:
	MOV AH,4CH
	INT 21H
CODE ENDS
END START